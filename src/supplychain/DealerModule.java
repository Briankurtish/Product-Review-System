    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        if(idTb.getText().isEmpty() || nameTb.getText().isEmpty() || qtyTb.getText().isEmpty() || priceTb.getText().isEmpty() ||supplierTb.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/stock", "root", "cipher2020");
            PreparedStatement add = con.prepareStatement("insert into products values(?,?,?,?,?)");
            add.setString(1, idTb.getText());
            add.setString(2, nameTb.getText());
            add.setString(3, qtyTb.getText());
            add.setInt(4, Integer.valueOf(priceTb.getText()));
            add.setString(5, supplierTb.getText());
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Added Successfully");
            DisplayProducts();
            Reset();
        }
        catch(Exception e){
            e.printStackTrace();
        }}
        
        
    }//GEN-LAST:event_addBtnActionPerformed

